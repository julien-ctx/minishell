#include "../../includes/minishell.h"

/***************************************************************************
Handles CTRL C and SIGINT signal
***************************************************************************/
void	ctrl_c(int sig)
{
	(void)sig;
	write(1, "\n", 1);
	rl_on_new_line();
	rl_redisplay();
}

/***************************************************************************
Handles CTRL \ and SIGQUIT signal
***************************************************************************/
void	ctrl_bs(int sig)
{
	(void)sig;
	rl_on_new_line();
	rl_redisplay();
}
